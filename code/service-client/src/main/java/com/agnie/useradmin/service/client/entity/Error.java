package com.agnie.useradmin.service.client.entity;

/**
 * @author Pandurang Patil 08-Feb-2014
 * 
 */
public class Error {
	private String	code;

	public Error() {
	}

	/**
	 * @param code
	 */
	public Error(String code) {
		super();
		this.code = code;
	}

	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}

}
